#include "stars.h"


void Stars::tick() {
  Mode::tick();
  int effective_width = width * strip.numPixels();

  for(int i = 0;i < effective_width;++i) {
    strip.setPixelColor(i, 0);
  }
  
  for(int i = 0;i < effective_width;++i) {
    strip.setPixelColor(random(strip.numPixels() - 1), Wheel(i));
    //strip.setPixelColor(i, Wheel(i));
  }
}







